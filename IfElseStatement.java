public class IfElseStatement {
    public static void main(String[] args) {
        int x=3;
        if(x==3){
            System.out.println("The value of x is 3");
        }
        else{
            System.out.println("The value of x is not 3");
        }
        System.out.println("This is printed no matter what");
    }
}
